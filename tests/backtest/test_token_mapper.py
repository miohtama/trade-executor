"""ERC-20 based trading pair loading tests."""

import datetime

from tradeexecutor.strategy.execution_context import unit_test_execution_context
from tradeexecutor.strategy.pandas_trader.token_mapper import create_trading_universe_for_tokens, TokenTuple
from tradeexecutor.strategy.universe_model import UniverseOptions
from tradingstrategy.chain import ChainId
from tradingstrategy.timebucket import TimeBucket


def test_load_token_data_polygon(persistent_test_client):
    """Load trading pair data based on ERC-20 address only."""
    client = persistent_test_client

    # Build a list of unique tokens
    polygon_token_list = {TokenTuple(ChainId.polygon, address) for address in POLYGON_TOKEN_DATA.strip().split()}

    # How many unique tokens we got
    assert len(polygon_token_list) == 271

    # Limit to 20 random tokens, as fetching caps at 75
    polygon_token_list = list(polygon_token_list)[0:20]

    universe_options = UniverseOptions(
        start_at=datetime.datetime(2023, 1, 1),
        end_at=datetime.datetime(2024, 1, 1),
    )

    strategy_universe = create_trading_universe_for_tokens(
        client,
        unit_test_execution_context,
        universe_options,
        TimeBucket.d7,
        polygon_token_list,
        reserve_token="0x2791bca1f2de4661ed88a30c99a7a9449aa84174",  # USDC.e bridged on Polygon
        intermediate_token="0x0d500b1d8e8ef31e21c99d1db9a6444d3adf1270",  # WMATIC on Polygon
    )

    # TODO: There is some flakiness here,
    # varies between 16, 18, 13 etc.
    assert strategy_universe.get_pair_count() >= 5

    for p in strategy_universe.iterate_pairs():
        print(p.base.address)


